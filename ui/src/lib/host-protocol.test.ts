describe("lighting config blob codec", () => {
  const sample: LightingConfig = {
    togglePersistMask: 0x80,
    toggleInitialState: 0x81,
    records: [
      {
        activation: { kind: "always" },
        cells: [
          {
            key: 0,
            effect: { kind: "solid", r: 10, g: 20, b: 30, periodMs: 0, phaseMs: 0, dutyPercent: 0 },
          },
        ],
      },
      { activation: { kind: "layerActive", layer: 7 }, cells: [] },
      { activation: { kind: "toggle", id: 31 }, cells: [] },
    ],
  };

  it("matches the CRC-32/ISO-HDLC reference vector", () => {
    expect(crc32(new TextEncoder().encode("123456789"))).toBe(0xcbf43926);
    expect(crc32(new Uint8Array(0))).toBe(0);
  });

  it("round-trips and is byte-stable", () => {
    const bytes = encodeLightingConfig(sample);
    const decoded = decodeLightingConfig(bytes);
    expect(decoded).toEqual(sample);
    expect(toHex(encodeLightingConfig(decoded))).toBe(toHex(bytes));
  });

  it("rejects corruption and invalid fields", () => {
    const bytes = encodeLightingConfig(sample);
    const corrupt = bytes.slice();
    corrupt[bytes.length - 1] ^= 0xff;
    expect(() => decodeLightingConfig(corrupt)).toThrow("crc mismatch");
    const badMagic = bytes.slice();
    badMagic[0] = 0;
    expect(() => decodeLightingConfig(badMagic)).toThrow("bad config magic");
    expect(() => decodeLightingConfig(bytes.subarray(0, 10))).toThrow(ProtocolError);
    expect(() =>
      encodeLightingConfig({
        ...sample,
        records: [{ activation: { kind: "layerActive", layer: 8 }, cells: [] }],
      }),
    ).toThrow("out of range");
    expect(() =>
      encodeLightingConfig({
        ...sample,
        records: [
          {
            activation: { kind: "always" },
            cells: [
              sample.records[0].cells[0],
              sample.records[0].cells[0],
            ],
          },
        ],
      }),
    ).toThrow("appears twice");
  });

  it("caps the blob at MAX_CONFIG_BLOB_LEN", () => {
    const full: LightingConfig = {
      togglePersistMask: 0xffffffff,
      toggleInitialState: 0,
      records: Array.from({ length: 16 }, () => ({
        activation: { kind: "always" as const },
        cells: Array.from({ length: 40 }, (_, k) => ({
          key: k,
          effect: {
            kind: "blink" as const,
            r: 1,
            g: 2,
            b: 3,
            periodMs: 500,
            phaseMs: 0,
            dutyPercent: 50,
          },
        })),
      })),
    };
    expect(encodeLightingConfig(full).length).toBe(MAX_CONFIG_BLOB_LEN);
  });
});

describe("host protocol codec", () => {
  it("round-trips every request variant", () => {
    const requests: Request[] = [
      { command: "getCapabilities", clientMajor: 1, clientMinor: 0 },
      { command: "ping", data: Uint8Array.from([1, 2, 3]) },
      { command: "ping", data: new Uint8Array(0) },
      {
        command: "setCells",
        ttlMs: 0xffffffff,
        cells: [
          {
            key: 79,
            effect: { kind: "breathe", r: 1, g: 2, b: 3, periodMs: 65535, phaseMs: 0, dutyPercent: 0 },
          },
        ],
      },
      { command: "unsetCells", keys: [0, 40, 79] },
      { command: "clearOverlay" },
      { command: "readOverlay" },
      { command: "replaceOverlay", ttlMs: 0, cells: [] },
      { command: "getBrightness" },
      { command: "setBrightness", level: 255 },
      { command: "getToggle", id: 9 },
      { command: "setToggle", id: 9, state: true },
      { command: "configBegin", totalLen: 7148, blobCrc32: 0xdeadbeef },
      { command: "configData", offset: 1024, data: Uint8Array.from({ length: 100 }, () => 0xaa) },
      { command: "configData", offset: 0, data: new Uint8Array(0) },
      { command: "configCommit" },
      { command: "configAbort" },
      { command: "configRead", offset: 4096, maxLen: 1024 },
      { command: "getVersion" },
      { command: "keymapRead", layer: 7, startKey: 83, maxCount: 1 },
      { command: "keymapWrite", entries: [] },
      {
        command: "keymapWrite",
        entries: [
          { layer: 0, key: 0, keycode: 0x0004 },
          { layer: 7, key: 83, keycode: 0x5220 },
        ],
      },
      { command: "enterBootloader", magic: BOOTLOADER_MAGIC, target: "central" },
    ];
    for (const request of requests) {
      const bytes = encodeRequest(7, request);
      expect(decodeRequest(bytes)).toEqual({ requestId: 7, request });
    }
  });

  it("rejects malformed messages", () => {
    expect(() => decodeRequest(Uint8Array.from([0x77, 0, 0, 0]))).toThrow(ProtocolError);
    // Trailing garbage after the declared payload.
    expect(() => decodeRequest(Uint8Array.from([0x12, 1, 0, 0, 9]))).toThrow(
      "length field disagrees",
    );
    // Response flag missing.
    expect(() => decodeResponse(Uint8Array.from([0x12, 1, 0, 0, 0]))).toThrow(ProtocolError);
    // Unknown status.
    expect(() => decodeResponse(Uint8Array.from([0x92, 1, 0xee, 0, 0]))).toThrow(
      "unknown status",
    );
    // partialApply on a non-overlay command.
    expect(() => decodeResponse(Uint8Array.from([0xa0, 1, 0x05, 1, 0, 0]))).toThrow(
      "only valid on overlay writes",
    );
  });

  it("gates the capabilities extension on the persistent-config bit", () => {
    const base = {
      type: "capabilities" as const,
      protocolMajor: 1,
      protocolMinor: 1,
      ledCountLeft: 40,
      ledCountRight: 40,
      layerCapacity: 8,
      maxCellsPerOp: 80,
      effectMask: 0b111,
      overlayCellCapacity: 80,
      maxMessageLen: MAX_MESSAGE_LEN,
      featureBits: 0x3f,
      maxConfigBlobLen: 0,
      keymapRows: 0,
      keymapCols: 0,
      maxKeymapEntriesPerOp: 0,
    };
    const respond = (payload: ResponsePayload): Response => ({
      requestId: 1,
      command: "getCapabilities",
      status: "ok",
      payload,
    });
    const without = encodeResponse(respond(base));
    const withExtension = encodeResponse(
      respond({
        ...base,
        featureBits: 0x3f | FEATURE_PERSISTENT_CONFIG,
        maxConfigBlobLen: MAX_CONFIG_BLOB_LEN,
      }),
    );
    expect(withExtension.length).toBe(without.length + 4);
    expect(decodeResponse(without)).toEqual(respond(base));
    expect(decodeResponse(withExtension)).toEqual(
      respond({
        ...base,
        featureBits: 0x3f | FEATURE_PERSISTENT_CONFIG,
        maxConfigBlobLen: MAX_CONFIG_BLOB_LEN,
      }),
    );
  });

  it("gates the keymap extension on feature bit 7", () => {
    const base: ResponsePayload = {
      type: "capabilities",
      protocolMajor: 1,
      protocolMinor: 2,
      ledCountLeft: 40,
      ledCountRight: 40,
      layerCapacity: 8,
      maxCellsPerOp: 80,
      effectMask: 0b111,
      overlayCellCapacity: 80,
      maxMessageLen: MAX_MESSAGE_LEN,
      featureBits: 0x3f,
      maxConfigBlobLen: 0,
      keymapRows: 0,
      keymapCols: 0,
      maxKeymapEntriesPerOp: 0,
    };
    const respond = (payload: ResponsePayload): Response => ({
      requestId: 9,
      command: "getCapabilities",
      status: "ok",
      payload,
    });
    // Bit 7 without bit 6: the keymap extension sits directly at offset 16.
    const withKeymap = respond({
      ...base,
      featureBits: 0x3f | FEATURE_KEYMAP,
      keymapRows: 6,
      keymapCols: 14,
      maxKeymapEntriesPerOp: 84,
    });
    const without = encodeResponse(respond(base));
    const withExtension = encodeResponse(withKeymap);
    expect(withExtension.length).toBe(without.length + 4);
    expect(decodeResponse(withExtension)).toEqual(withKeymap);
  });

  it("skips capability extensions appended by newer protocol minors", () => {
    // A future minor with a hypothetical feature bit 9 appends its own
    // extension after the keymap extension; a v1.3 client must skip it, not
    // reject the handshake (PROTOCOL.md "Versioning": minor bumps are
    // additive).
    const expected: Response = {
      requestId: 7,
      command: "getCapabilities",
      status: "ok",
      payload: {
        type: "capabilities",
        protocolMajor: 1,
        protocolMinor: 4,
        ledCountLeft: 40,
        ledCountRight: 40,
        layerCapacity: 8,
        maxCellsPerOp: 80,
        effectMask: 0b111,
        overlayCellCapacity: 80,
        maxMessageLen: MAX_MESSAGE_LEN,
        featureBits: 0xff | (1 << 8) | (1 << 9),
        maxConfigBlobLen: MAX_CONFIG_BLOB_LEN,
        keymapRows: 6,
        keymapCols: 14,
        maxKeymapEntriesPerOp: 84,
      },
    };
    const withoutExtension = encodeResponse(expected);
    const futureExtension = Uint8Array.from([0xde, 0xad, 0xbe, 0xef]);
    const extended = new Uint8Array(withoutExtension.length + futureExtension.length);
    extended.set(withoutExtension);
    extended.set(futureExtension, withoutExtension.length);
    // Patch payload_len (u16 at offset 3) for the appended bytes.
    const view = new DataView(extended.buffer);
    view.setUint16(3, view.getUint16(3, true) + futureExtension.length, true);
    expect(decodeResponse(extended)).toEqual(expected);
  });

  it("round-trips config responses", () => {
    const responses: Response[] = [
      { requestId: 1, command: "configBegin", status: "ok", payload: { type: "empty" } },
      { requestId: 2, command: "configData", status: "noSession", payload: { type: "empty" } },
      { requestId: 3, command: "configData", status: "badOffset", payload: { type: "empty" } },
      {
        requestId: 4,
        command: "configCommit",
        status: "configIncomplete",
        payload: { type: "empty" },
      },
      { requestId: 5, command: "configCommit", status: "crcMismatch", payload: { type: "empty" } },
      {
        requestId: 6,
        command: "configCommit",
        status: "invalidConfig",
        payload: { type: "empty" },
      },
      {
        requestId: 7,
        command: "configRead",
        status: "ok",
        payload: { type: "configData", totalLen: 7148, data: Uint8Array.from([1, 2, 3]) },
      },
    ];
    for (const response of responses) {
      expect(decodeResponse(encodeResponse(response))).toEqual(response);
    }
    // The configData payload only pairs with configRead.
    expect(() =>
      encodeResponse({
        requestId: 8,
        command: "configCommit",
        status: "ok",
        payload: { type: "configData", totalLen: 0, data: new Uint8Array(0) },
      }),
    ).toThrow("does not match");
  });

  it("round-trips keymap and version responses and validates flags", () => {
    const central = {
      present: true,
      fwMajor: 0,
      fwMinor: 1,
      fwPatch: 0,
      gitHashHex: "3161326233633464", // "1a2b3c4d"
      dirty: false,
    };
    const neverSeen = {
      present: false,
      fwMajor: 0,
      fwMinor: 0,
      fwPatch: 0,
      gitHashHex: "0000000000000000",
      dirty: false,
    };
    const responses: Response[] = [
      {
        requestId: 1,
        command: "getVersion",
        status: "ok",
        payload: { type: "version", central, peripheral: neverSeen, halvesMismatch: false },
      },
      {
        requestId: 2,
        command: "keymapRead",
        status: "ok",
        payload: { type: "keymapActions", layer: 3, startKey: 10, keycodes: [4, 0, 0x7e1f] },
      },
      {
        requestId: 3,
        command: "keymapWrite",
        status: "ok",
        payload: { type: "keymapWritten", keycodes: [] },
      },
      { requestId: 4, command: "keymapWrite", status: "outOfRange", payload: { type: "empty" } },
    ];
    for (const response of responses) {
      expect(decodeResponse(encodeResponse(response))).toEqual(response);
    }

    expect(gitHashText(central.gitHashHex)).toBe("1a2b3c4d");
    expect(gitHashText(neverSeen.gitHashHex)).toBe("");

    // present / dirty / halves_mismatch must be 0 or 1 on the wire.
    const encoded = encodeResponse(responses[0]);
    const corrupt = encoded.slice();
    corrupt[RESPONSE_HEADER_LEN] = 2; // central present byte
    expect(() => decodeResponse(corrupt)).toThrow("must be 0 or 1");
    const badMismatch = encoded.slice();
    badMismatch[encoded.length - 1] = 7; // halves_mismatch byte
    expect(() => decodeResponse(badMismatch)).toThrow("must be 0 or 1");
  });

  it("rejects mismatched response payloads", () => {
    expect(() =>
      encodeResponse({
        requestId: 1,
        command: "ping",
        status: "ok",
        payload: { type: "brightness", level: 3 },
      }),
    ).toThrow("does not match");
    expect(() =>
      encodeResponse({
        requestId: 1,
        command: "ping",
        status: "busy",
        payload: { type: "echo", data: new Uint8Array(0) },
      }),
    ).toThrow("does not match");
  });
});

describe("host protocol frame layer", () => {
  it("enforces sequencing and restarts on sequence zero", () => {
    const message = Uint8Array.from({ length: 40 }, (_, i) => i);
    const frames = splitFrames(message, 20);
    expect(frames.length).toBe(3);

    const reassembler = new Reassembler();
    expect(reassembler.push(frames[0])).toBeNull();
    expect(() => reassembler.push(frames[2])).toThrow("expected frame sequence 1, got 2");

    // A fresh sequence-0 frame drops the incomplete message.
    expect(reassembler.push(frames[0])).toBeNull();
    const restart = splitFrames(Uint8Array.from([9]), 20);
    expect(reassembler.push(restart[0])).toEqual(Uint8Array.from([9]));
  });

  it("ignores padding via the per-frame length byte", () => {
    const [frame] = splitFrames(Uint8Array.from([5, 6]), 32, true);
    expect(frame.length).toBe(32);
    expect(new Reassembler().push(frame)).toEqual(Uint8Array.from([5, 6]));
  });

  it("rejects bad frames and oversized messages", () => {
    const reassembler = new Reassembler();
    expect(() => reassembler.push(Uint8Array.from([0]))).toThrow(ProtocolError);
    expect(() => reassembler.push(Uint8Array.from([0x80, 0]))).toThrow("zero-length");
    expect(() => reassembler.push(Uint8Array.from([0x80, 5, 1]))).toThrow(
      "shorter than declared",
    );
    expect(() => splitFrames(new Uint8Array(0), 20)).toThrow("empty message");
    expect(() => splitFrames(Uint8Array.from([1]), 2)).toThrow("below minimum");
    expect(() => splitFrames(new Uint8Array(MAX_MESSAGE_LEN), 3)).toThrow("exceeds 128 frames");
  });
});
