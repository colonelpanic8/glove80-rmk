interface MessageVector {
  name: string;
  kind: "request" | "response";
  requestId: number;
  message: Record<string, unknown>;
  hex: string;
}

interface FrameVector {
  name: string;
  sourceMessage: string;
  chunkSize: number;
  padded: boolean;
  messageHex: string;
  framesHex: string[];
}

interface ConfigVector {
  name: string;
  config: LightingConfig;
  hex: string;
}

interface InvalidConfigVector {
  name: string;
  hex: string;
  error: string;
}

interface GoldenDoc {
  protocol: { major: number; minor: number };
  messages: MessageVector[];
  frames?: FrameVector[];
  configs?: ConfigVector[];
  invalidConfigs?: InvalidConfigVector[];
}

const golden: GoldenDoc = JSON.parse(readFileSync(vectorsPath, "utf8"));
const goldenV11: GoldenDoc = JSON.parse(readFileSync(vectorsV11Path, "utf8"));
const goldenV12: GoldenDoc = JSON.parse(readFileSync(vectorsV12Path, "utf8"));
const goldenV13: GoldenDoc = JSON.parse(readFileSync(vectorsV13Path, "utf8"));

function unhex(hex: string): Uint8Array {
  expect(hex.length % 2).toBe(0);
  const out = new Uint8Array(hex.length / 2);
  for (let i = 0; i < out.length; i++) {
    out[i] = parseInt(hex.slice(i * 2, i * 2 + 2), 16);
  }
  return out;
}

function toHex(bytes: Uint8Array): string {
  return [...bytes].map((b) => b.toString(16).padStart(2, "0")).join("");
}

/** The vectors carry byte strings as `dataHex`; the runtime API uses
 * Uint8Array. Everything else matches the runtime shape directly. */
function vectorRequest(message: Record<string, unknown>): Request {
  const { dataHex, ...rest } = message as { dataHex?: string } & Record<string, unknown>;
  if (dataHex !== undefined) {
    return { ...rest, data: unhex(dataHex) } as Request;
  }
  return rest as unknown as Request;
}

function vectorResponse(requestId: number, message: Record<string, unknown>): Response {
  const payloadJson = message.payload as { type: string; dataHex?: string } & Record<
    string,
    unknown
  >;
  const { dataHex, ...payloadRest } = payloadJson;
  // The wire (and the vectors) omit feature-gated capability extensions;
  // the runtime shape carries 0 for absent fields.
  if (payloadRest.type === "capabilities") {
    payloadRest.maxConfigBlobLen ??= 0;
    payloadRest.keymapRows ??= 0;
    payloadRest.keymapCols ??= 0;
    payloadRest.maxKeymapEntriesPerOp ??= 0;
  }
  const payload = (
    dataHex !== undefined ? { ...payloadRest, data: unhex(dataHex) } : payloadRest
  ) as ResponsePayload;
  return {
    requestId,
    command: message.command as CommandName,
    status: message.status as StatusName,
    payload,
  };
}

function assertMessageVector(vector: MessageVector): void {
  if (vector.kind === "request") {
    const request = vectorRequest(vector.message);
    expect(toHex(encodeRequest(vector.requestId, request))).toBe(vector.hex);
    expect(decodeRequest(unhex(vector.hex))).toEqual({
      requestId: vector.requestId,
      request,
    });
  } else {
    const response = vectorResponse(vector.requestId, vector.message);
    expect(toHex(encodeResponse(response))).toBe(vector.hex);
    expect(decodeResponse(unhex(vector.hex))).toEqual(response);
  }
}

describe("host protocol golden vectors (v1.0, frozen)", () => {
  it("targets the codec's protocol major version", () => {
    // The v1.0 file is frozen at minor 0; the codec's minor only grows.
    expect(golden.protocol.major).toBe(PROTOCOL_VERSION_MAJOR);
    expect(golden.protocol.minor).toBe(0);
    expect(golden.protocol.minor).toBeLessThanOrEqual(PROTOCOL_VERSION_MINOR);
    expect(golden.messages.length).toBeGreaterThan(0);
    expect(golden.frames?.length).toBeGreaterThan(0);
  });

  for (const vector of golden.messages) {
    it(`encodes and decodes ${vector.name}`, () => {
      assertMessageVector(vector);
    });
  }

  for (const vector of golden.frames ?? []) {
    it(`splits and reassembles ${vector.name}`, () => {
      const message = unhex(vector.messageHex);
      const frames = splitFrames(message, vector.chunkSize, vector.padded);
      expect(frames.map(toHex)).toEqual(vector.framesHex);

      const reassembler = new Reassembler();
      vector.framesHex.forEach((frameHex, i) => {
        const result = reassembler.push(unhex(frameHex));
        if (i === vector.framesHex.length - 1) {
          expect(result).toEqual(message);
        } else {
          expect(result).toBeNull();
        }
      });
    });
  }
});

